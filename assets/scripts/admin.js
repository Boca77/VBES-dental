/* deterministic 0..1 from a string (for stable "taken" slots) */
function hash01(str) {
  let h = 2166136261;
  for (let i = 0; i < str.length; i++) { h ^= str.charCodeAt(i); h = Math.imul(h, 16777619); }
  return ((h >>> 0) % 1000) / 1000;
}

function ymd(d) {
  return d.getFullYear() + "-" + String(d.getMonth() + 1).padStart(2, "0") + "-" + String(d.getDate()).padStart(2, "0");
}
function parseYmd(s) { const [y, m, d] = s.split("-").map(Number); return new Date(y, m - 1, d); }

/* working hours → slot list (minutes-of-day strings "HH:MM") */
function slotsForDate(d) {
  const wd = d.getDay();              // 0 Sun .. 6 Sat
  if (wd === 0) return [];            // closed Sunday
  const open = 12 * 60;
  const close = (wd === 6 ? 17 : 19) * 60;
  const brkS = 15 * 60, brkE = 16 * 60;
  const out = [];
  for (let t = open; t + 30 <= close; t += 30) {
    if (t >= brkS && t < brkE) continue;
    out.push(String(Math.floor(t / 60)).padStart(2, "0") + ":" + String(t % 60).padStart(2, "0"));
  }
  return out;
}

