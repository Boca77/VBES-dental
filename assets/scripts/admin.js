  /* ---- calendar ---- */
  function renderCalendar() {
    const L = window.I18N[window.LANG];
    const v = state.view;
    $root.find(".cal-title").text(L.months[v.getMonth()] + " " + v.getFullYear());
    const first = new Date(v.getFullYear(), v.getMonth(), 1);
    const startDow = first.getDay();
    const daysIn = new Date(v.getFullYear(), v.getMonth() + 1, 0).getDate();
    const today = new Date(); today.setHours(0, 0, 0, 0);
    const apptDays = {};
    loadAppts().forEach(a => { apptDays[a.date] = true; });

    const $body = $root.find(".cal-body").empty();
    L.days.forEach(dn => $body.append(`<div class="cal-dow">${dn.slice(0, 2)}</div>`));
    for (let i = 0; i < startDow; i++) $body.append('<div class="cal-cell empty"></div>');
    for (let day = 1; day <= daysIn; day++) {
      const d = new Date(v.getFullYear(), v.getMonth(), day);
      const key = ymd(d);
      const isSun = d.getDay() === 0;
      const past = d < today;
      const beyond = d > maxDate;
      const disabled = isSun || past || beyond;
      const $c = $(`<button class="cal-cell">${day}</button>`);
      if (d.getTime() === today.getTime()) $c.addClass("today");
      if (state.date === key) $c.addClass("sel");
      if (apptDays[key]) $c.append('<span class="pip"></span>');
      if (disabled) $c.prop("disabled", true);
      else $c.on("click", () => {
        state.date = key; state.time = null;
        $body.find(".cal-cell").removeClass("sel");
        $c.addClass("sel");
        renderSlots(); updateNav();
      });
      $body.append($c);
    }
    // nav arrows enable/disable
    const prevMonthEnd = new Date(v.getFullYear(), v.getMonth(), 0);
    $root.find(".cal-prev").prop("disabled", prevMonthEnd < today);
    const nextMonthStart = new Date(v.getFullYear(), v.getMonth() + 1, 1);
    $root.find(".cal-next").prop("disabled", nextMonthStart > maxDate);
  }

  function renderSlots() {
    const $list = $root.find(".slot-list").empty();
    if (!state.date) {
      $list.append(`<div class="slot-empty">${t("book.choose_date")}</div>`);
      return;
    }
    const d = parseYmd(state.date);
    const slots = slotsForDate(d);
    if (!slots.length) {
      $list.append(`<div class="slot-empty">${t("book.closed")}</div>`);
      return;
    }
    const now = new Date();
    const isToday = ymd(now) === state.date;
    const booked = {};
    loadAppts().forEach(a => { if (a.date === state.date) booked[a.time] = true; });
    let any = false;
    slots.forEach(tm => {
      const [hh, mm] = tm.split(":").map(Number);
      const passed = isToday && (hh * 60 + mm) <= (now.getHours() * 60 + now.getMinutes());
      const taken = booked[tm] || hash01(state.date + tm) < 0.22;
      const off = passed || taken;
      const $s = $(`<button class="slot">${tm}</button>`);
      if (state.time === tm) $s.addClass("sel");
      if (off) $s.prop("disabled", true);
      else { any = true; $s.on("click", () => { state.time = tm; $list.find(".slot").removeClass("sel"); $s.addClass("sel"); updateNav(); }); }
      $list.append($s);
    });
    if (!any) $list.append(`<div class="slot-empty">${t("book.no_slots")}</div>`);
  }

